/** let: multaveis, ou seja o seu valor pode ser alterado durante a execução do programa */
let customer = "João"
console.log("Nome do cliente:", customer)

customer = "Pedro"
console.log("O nome do cliente mudou:", customer)

/** const: imultaveis, uma vez atribuído o valor ela não pode ser alterada */
const coffe = "Expresso"
console.log("Pedido:", coffe)
